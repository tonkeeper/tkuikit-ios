import UIKit

public extension UIWindow {
  func applyThemeMode(_ themeModel: ThemeMode) {
    switch themeModel {
    case .blue:
      overrideUserInterfaceStyle = .light
    case .dark:
      overrideUserInterfaceStyle = .dark
    case .system:
      overrideUserInterfaceStyle = .unspecified
    }
  }
}
